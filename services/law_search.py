async def find_relevant_laws(user_query: str, limit: int = config.LAW_SEARCH_LIMIT) -> list[dict]:
    """
    Ищет релевантные статьи закона в FTS5-индексе по тексту запроса
    пользователя. Возвращает список словарей (codex, article_number,
    article_title, article_text).
    """
    fts_query = build_fts_query(user_query)
    if not fts_query:
        return []

    results = await db.search_laws(fts_query, limit)
    if results:
        return results

    lowered = user_query.lower()
    for codex, hints in BRANCH_HINTS.items():
        if any(h in lowered for h in hints):
            codex_safe = codex.replace('"', "")
            fallback_query = f'codex:"{codex_safe}"'
            results = await db.search_laws(fallback_query, limit)
            if results:
                return results
            matched_hints = [h for h in hints if h in lowered]
            if matched_hints:
                hint_query = " OR ".join(f"{h}*" for h in matched_hints)
                results = await db.search_laws(hint_query, limit)
                if results:
                    return results
    return []
